package item;

public class BossItem {
    private  String label;
    private String name;
    private int all;

    public String getName() {
        return name;
    }

    public int getAll() {
        return all;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAll(int all) {
        this.all = all;
    }
}
